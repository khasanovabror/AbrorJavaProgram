package com.java.class03;

public class DivisionOperator {
    public static void main(String [] args){
        System.out.println(10.0/20);
        System.out.println(13.0/10);

        System.out.println(51/10);
        System.out.println(1/2.0);

        System.out.println(99/10);  //when dividing by 10 it just removes the last digit so 99/10 = 9
        System.out.println(2/10);   //when dividing by 10 it just removes the last digit so 2/10 = 0 because only one digit
        System.out.println(43/10);  //when dividing by 10 it just removes the last digit so 43/10 = 4
        System.out.println(12/10);
    }
}
