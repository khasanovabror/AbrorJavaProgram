package com.java.class03;

public class OperatorsExamples {
    public static void main(String args[]){
        int a=10, b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(b/a);
        System.out.println(10 % 3);
        System.out.println(4*10-3*20+45/3); //40-60+15
        System.out.println(50/10*5*10/2+5*4/8);
        System.out.println(15*(3+2)*2/10);
    }
}
