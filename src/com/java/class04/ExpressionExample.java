package com.java.class04;

public class ExpressionExample {
    public static void main(String args[]){
        System.out.println(4*10-3*20+45/3); //40-60+15
        System.out.println(50/10*5*10/2+5*4/8); //(50/10)*(5)*(10)/(2)=125 + (5*4)/(8) = 2.5
        System.out.println(15*(3+2)*2/10);
    }
}
